#include "../minishell.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	size_t	len;
	size_t	i;
	size_t	j;
	char	*new;

	i = 0;
	j = 0;
	len = 0;
	len += ft_strlen(s1);
	len += ft_strlen(s2);
	new = malloc(sizeof(*s1) * (len + 1));
	if (new == 0)
		return (NULL);
	while (s1[i] != '\0')
	{
		new[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		new[i + j] = s2[j];
		j++;
	}
	new[i + j] = '\0';
	return (new);
}
